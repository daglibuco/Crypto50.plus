// Helper to generate mock data for the 300+ coins without hitting an API
const generateMockData = (symbol: string, name: string, category: string): Coin => {
  let hash = 0;
  for (let i = 0; i < symbol.length; i++) {
    hash = symbol.charCodeAt(i) + ((hash << 5) - hash);
  }
  
  const priceTier = Math.abs(hash % 4); 
  let price = 0;
  if (["BTC", "YFI", "PAXG", "MKR", "ETH"].includes(symbol)) {
    price = 2000 + (Math.abs(hash) % 60000); 
  } else if (priceTier === 0) price = 0.05 + (Math.abs(hash) % 1); 
  else if (priceTier === 1) price = 1 + (Math.abs(hash) % 10);    
  else if (priceTier === 2) price = 10 + (Math.abs(hash) % 90);   
  else price = 100 + (Math.abs(hash) % 400);                      

  const volatility = Math.abs(hash) % 10 > 7 ? TrafficLight.RED : (Math.abs(hash) % 10 > 4 ? TrafficLight.YELLOW : TrafficLight.GREEN);
  const change24h = (Math.abs(hash) % 200 - 100) / 10; 

  const website = `https://${name.toLowerCase().replace(/\s/g, '')}.org`;
  const whitepaper = `https://${name.toLowerCase().replace(/\s/g, '')}.org/whitepaper.pdf`;
  
  // High quality repository from SpotHQ containing over 500+ coins. 
  // This is much more comprehensive than CoinCap for the full list of 300+ coins.
  const image = `https://raw.githubusercontent.com/spothq/cryptocurrency-icons/master/128/color/${symbol.toLowerCase()}.png`;

  return { symbol, name, category, price, change24h, volatility, website, whitepaper, image };
};

